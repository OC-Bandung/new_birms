<p><strong>Future development plans:</strong></p>
<p>The Government of Bandung City encourages users from all sectors of society (government, private sector, civil society) to visit the website regularly, subscribe to updates on tender opportunities or information on specific contracts, and put this data into use. The Government of Bandung City is committed to improving the quality of the disclosed data over time, and welcomes any feedback from users on data quality. Bandung City is working to introduce geolocation data of procurement packages, and is currently piloting this initiative on 2018 data. Geolocation data is included in the 2018 dataset and API, and can also be visualized on the interactive map.</p>
