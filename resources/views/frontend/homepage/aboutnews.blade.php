<p><strong>About the data:</strong></p>
<p>The data in this portal covers Bandung Cityâ€™s procurement and contracting processes conducted through both competitive and non-competitive procurement methods for years 2016, 2017 and 2018. Data is drawn from several procurement systems, including: SIMDA (e-budget), SiRUP (e-procurement planning), LPSE (e-tender), and e-kontrak (contract details for non-competitive procurement).</p>
